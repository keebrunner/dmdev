package com.dmdev.week5.Collections1.Task1;

public class Chat {
    private String name;
    private int numUsers;


    public Chat(String name, int numUsers) {
        this.name = name;
        this.numUsers = numUsers;
    }

    public String getName() {
        return name;
    }

    public int getNumUsers() {
        return numUsers;
    }
}
